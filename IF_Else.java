class IF_Else {
    public static void main(String[] args) {
        
    int mark=90;
    if(mark>=90){
        System.out.println("A grade");
    }
    else if(mark>=80){
        System.out.println("B grade");
    }
    else if(mark>=70){
        System.out.println("C grade");
    }
    else if(mark>=60){
        System.out.println("D grade");
    }
    else{
        System.out.println("F grade");
    }
}
}